# Problem 01.03.01: Khai báo và kiểm tra biến
# Khai báo các biến
student_name = "Nguyễn Văn A"
student_age = 17
student_height = 1.68
is_good_at_math = True

# In thông tin
print("Tên:", student_name)
print("Tuổi:", student_age)
print("Chiều cao:", student_height)
print("Giỏi toán:", is_good_at_math)

# Kiểm tra kiểu dữ liệu
print("\nKiểu dữ liệu:")
print("Tên:", type(student_name))
print("Tuổi:", type(student_age))
print("Chiều cao:", type(student_height))
print("Giỏi toán:", type(is_good_at_math))